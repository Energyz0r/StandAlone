    // ── SEARCH ──────────────────────────────────────────────────────────
    private void searchQuotes() {
        String term = txtSearchTerm.getText().trim();
        String searchBy = (String) cboSearchBy.getSelectedItem();

        if (term.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Please enter a search term.",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String column = "Author".equals(searchBy) ? "Author" : "Quote";
        String sql = "SELECT * FROM Quotations WHERE " + column + " LIKE ? ORDER BY ID";
        StringBuilder results = new StringBuilder();

        try (Connection conn = getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, "%" + term + "%");
            try (ResultSet rs = pstmt.executeQuery()) {
                int count = 0;
                while (rs.next()) {
                    count++;
                    results.append("ID: ").append(rs.getInt("ID")).append("\n");
                    results.append("Quote: ").append(rs.getString("Quote")).append("\n");
                    results.append("Author: ").append(rs.getString("Author")).append("\n");
                    results.append("\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500"
                                 + "\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500"
                                 + "\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n");
                }
                if (count == 0) {
                    results.append("No quotes found matching \"").append(term)
                          .append("\" in ").append(column).append(".");
                } else {
                    results.insert(0, "Found " + count + " result(s) for \""
                                       + term + "\" in " + column + ":\n\n");
                }
            }
        } catch (SQLException e) {
            results.append("Error: ").append(e.getMessage());
        }

        txtareaSearchResults.setText(results.toString());
    }

