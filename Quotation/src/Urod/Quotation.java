    // ── DELETE ──────────────────────────────────────────────────────────
    private void deleteQuote() {
        int index = cboDeleteQuotes.getSelectedIndex() - 1;
        if (index < 0) {
            JOptionPane.showMessageDialog(this,
                "Please select a quote to delete.",
                "Selection Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Quote q = deleteQuotesList.get(index);
        int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete this quote?\n\n"
            + "\"" + (q.text.length() > 60 ? q.text.substring(0, 60) + "..." : q.text) + "\"\n"
            + "— " + q.author,
            "Confirm Delete", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm != JOptionPane.YES_OPTION) return;

        String sql = "DELETE FROM Quotations WHERE ID = ?";
        try (Connection conn = getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setInt(1, q.id);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this,
                "Quote deleted successfully!",
                "Success", JOptionPane.INFORMATION_MESSAGE);
            refreshAll();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Error deleting quote:\n" + e.getMessage(),
                "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

