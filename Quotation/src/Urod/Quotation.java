    private void initEventHandlers() {

        // ── Read Tab ────────────────────────────────────────────────────
        cboReadQuotes.addActionListener(evt -> {
            int index = cboReadQuotes.getSelectedIndex() - 1;
            if (index >= 0 && index < readQuotesList.size()) {
                Quote q = readQuotesList.get(index);
                txtareaReadQuotations.setText(q.text);
                lblReadAuthor.setText("Author: " + q.author);
            } else {
                txtareaReadQuotations.setText("");
                lblReadAuthor.setText("Author");
            }
        });

        btnRefresh.addActionListener(evt -> loadReadQuotes());

        // ── Insert Tab ──────────────────────────────────────────────────
        btnInsert.addActionListener(evt -> insertQuote());

        // ── Delete Tab ──────────────────────────────────────────────────
        cboDeleteQuotes.addActionListener(evt -> {
            int index = cboDeleteQuotes.getSelectedIndex() - 1;
            if (index >= 0 && index < deleteQuotesList.size()) {
                Quote q = deleteQuotesList.get(index);
                txtareaDeletePreview.setText(q.text);
                lblDeleteAuthorInfo.setText(q.author);
            } else {
                txtareaDeletePreview.setText("");
                lblDeleteAuthorInfo.setText(" ");
            }
        });

        btnDeleteRefresh.addActionListener(evt -> loadDeleteQuotes());
        btnDelete.addActionListener(evt -> deleteQuote());

        // ── Update Tab ──────────────────────────────────────────────────
        cboUpdateQuotes.addActionListener(evt -> {
            int index = cboUpdateQuotes.getSelectedIndex() - 1;
            if (index >= 0 && index < updateQuotesList.size()) {
                Quote q = updateQuotesList.get(index);
                txtareaUpdateQuote.setText(q.text);
                txtUpdateAuthor.setText(q.author);
            } else {
                txtareaUpdateQuote.setText("");
                txtUpdateAuthor.setText("");
            }
        });

        btnUpdateRefresh.addActionListener(evt -> loadUpdateQuotes());
        btnUpdate.addActionListener(evt -> updateQuote());

        // ── Search Tab ──────────────────────────────────────────────────
        btnSearch.addActionListener(evt -> searchQuotes());

        // ── Close ───────────────────────────────────────────────────────
        btnClose.addActionListener(evt -> System.exit(0));
    }

